<p class="lead"><br/><br/>
  
  Let’s begin chapter 7 by discussing silence, or the pause.  The pause is an effective tool in many ways:<br/><br/>
  

  •  To calm down your nervous and anxiety
  •  Provides a breath of fresh air – which will reduce your heart rate and help control your nervousness and anxiety.
  •  Replaces filler words such as – Ah, you know, um, like, and
  •  It allows the audience to take in, soak up, process, and digest the information you’ve just delivered.
  •  It allows you to take a second to move around the room without distraction.  i.e – Pause, take a few steps to your next position, and continue on to your next point, thus placing more emphasis on that point.  
  •  To help separate sections of your speech.
  •  For transitions.  Use the pause after each section of your speech.  
  
  Example:<br/><br/>
  



  Open (Pause)
  1st. point (pause)
  2nd point (pause)
  3rd point (pause)
  Close<br/><br/>
   

  •  Attention:  If you feel like you’re loosing the audiences’ attention, pause to gain it back.
  •  Arouse interest into what you will say next.  i.e – If you say something important, a sentence that is shocking, filled with emotion, a stat,  or fact, state your point and pause.  Let the audience sit on that point thus creating anticipation into what you will say next.
  •  It gives off the impression that you’re reflecting on that particular point/message, so the audience should be reflecting, thinking of that point as well.<br/><br/>
    

  Those are just some of the benefits of using the pause.  The pause will you organize your thoughts while improving the flow and clarity of your speech/presentation.<br/><br/>

  Controlling speed:   Before and during your presentation<br/><br/>
  

  The length of your presentation will differ when practicing at home compared to when presenting in front of an audience. This is the result of your nerves and anxiety.<br/><br/>
  
  On the other hand, when you become more comfortable with presenting, you start to expand on points when you would normally stop to reach the end.<br/><br/>
  
  Be conscious of your speed throughout your presentation and use the pause to breath.<br/><br/>
  
  A tip that I suggest is to time your speech for less than your maximum time limit.  The reason behind this is:<br/><br/>
  
   A: To leave room to interact with the audience<br/><br/>
   
  B: If you begin to drag on a certain point, you will have a buffer to accommodate for this added time. <br/><br/>
  
  C:  It’s much better to be slightly below your time than exceed your time.  The audience members quickly become restless.<br/><br/>
    
  Overall:  Practice, practice, and some more practice.  Rehearse your speech until you accomplish a consistent time that is slightly less than your given time.<br/><br/>
  
  You will find that your speed and rate become more controlled once you’ve built up your confidence.<br/><br/>
  
  Always be aware of the clock in the room or wear a wristwatch to glance at the time.  This will ensure you’re on track.  If you go well beyond your time or not enough, you either cause your audience to become restless or the credibility of your speech will be compromised due to the lack of content/information.<br/><br/>
  
  Try to avoid rushing through your speech at all costs.  If you notice that you’re rushing:  Pause, take a breath, and continue.<br/><br/>
   
  <b>Vocal Variety</b>

  Your voice is your tool for communicating to the world.  Your voice in your presentation determines the quality and effectiveness’ that you will convey.<br/><br/>
    
  During rehearsal and practice, watch for the following.<br/><br/>
  
  •  Volume – Can your audience members at the back of the room hear you?  Are you screaming at the audience members at the front of the room?
  •  Tone – Monotone, A high pitch, shrill, strong, 
  •  Rate – Slow vs. Fast
  •  Quality – Friendly, natural, sincere<br/><br/>
  
  <b>Tone</b>

  Monotone will result in boredom, and a high pitch may cause irritation.  Change your tone based on the emphasis your want to place on certain words.<br/><br/>
  

  If you’re excited:  High pitch 
  If you’re sad: Slow and lazy tone.<br/><br/>
  
  Is your voice:<br/><br/>
  
  •  Friendly
  •  Sincere
  •  Strong
  •  Energetic & Expressive<br/><br/>
  
  This all depends on the nature of your speech topic and/or audience.<br/><br/>
  
  Ex –<br/><br/>
  
  Speaking to clients – Friendly and sincere
  Speaking on a topic that demands a call to action – Use a strong, energetic, and an expressive tone.<br/><br/>
  
  Consider these concepts when constructing your speech<br/><br/>
  
  For ex –<br/><br/>
   
   Tone:  we MUST not change this policy,<br/><br/>
   
  Or <br/><br/>
  
  Small, tiny, bite, sized, cookies..<br/><br/>
  
  <b>Rate</b>

  Speak slow enough so your audience understands you, but speak at a rate that is natural sounding and comfortable.  Depending on your speech topic/message, you may need to speed up or slow down at times within your speech.  Changing the rate adds variety to your message.<br/><br/>
   
  <b>Volume</b><br/><br/>
  

  Similar rules apply.  Speak loud enough so the people at the back of the room can hear you, however, don’t speak so loud that it sounds like you’re yelling.  As well, avoid speaking so soft that no one in the room can hear you.<br/><br/>


  <b>Quality</b><br/><br/>

  As discussed, alter your voice to the audience and message.  Avoid the sound of rushing through, or that you’re angry, nasally, or breathy.  Review your videos to evaluate the quality of your voice.<br/><br/>

  Your voice should be easy to listen to while adding variety  (In rates, tone, and volume) to maintain the audiences’ attention and comprehension.<br/><br/>

  Remember to focus and be conscious of your posture.  By maintaining better posture, you will improve your breathing, which will help with your tone, rate, volume, and quality of your voice.<br/><br/> 


  <b>Ex –</b><br/><br/>

  <i>“Change!  We must change this policy now or we will all be doomed!”</i><br/><br/>

  <i>“It was a cold, rainy evening as we sat quiet in front of the fire” and all of a sudden we here a BANG, and a scream….”</i><br/><br/>

  <i>“ We must act now!  If we do not act, we will face the consequences”</i><br/><br/>

  When constructing and rehearsing your speech, I recommend placing a note or a (*), (!) around areas that call for a change in rate, tone, volume – vocal variety.<br/><br/>

  The more passion, enthusiasm, energy, and excitement you have in your voice, the more passion, enthusiasm, energy, and excitement you will convey to your audience.  Which will:<br/><br/>

  •  Increase the audiences’ attention
  •  Comprehension
  •  Effect you will have on the audience<br/><br/>

  Please note:  Ensure to convey your enthusiasm, energy, and excitement naturally and sincerely.  Whichever topic you’re given or chosen for you, attempt to find a deeper level of interest to ensure an honest delivery.<br/><br/>

  Be you times 10, but be truly excited to present your thoughts, express your opinions, share your knowledge/research.  If you need to, use bigger gestures, smile more, and make specific word choices to emphasis your meaning.<br/><br/>

  The above criterion is necessary whenever persuading, informing, or attempting to inspire the audience.  You will never see a successful presenters/speaker who doesn’t express his/her message with passion, energy, excitement, and enthusiasm in his/her words.<br/><br/>

  Over all, have a voice, gestures, and body language that conveys authority, confidence, and energy.<br/><br/>


 
