<p class="lead"><br/><br/>

  <i>“Research indicates that roughly 63% of communication is dependent on body language and vocal tone”</i>

  Combining strong words with:<br/><br/>

  •  Smooth gestures
  •  Facial expressions
  •  Eye contact
  •  Stance
  •  Movement<br/><br/>

  You will increase the quality and conviction of your speech.<br/><br/>

  Let’s look deeper into hand gestures, facial expressions, stance, eye contact and movement.<br/><br/>

  <b>Hand gestures</b>

  Use your hands to speak.  If you’re describing something large and round, slightly move your body back while expanding your hands as if you’re hugging a large tree.<br/><br/>  

  If you’re describing something small, use your fingers as if you’re pinching something – bringing your fingers together at eye level describing how small something is.<br/><br/>

  Other examples in which hand gestures could be used to describe something:<br/><br/>

  •  Long 
  •  Short
  •  Big
  •  Small<br/><br/>

  Wipe your head if you’re describing something hot, or clasp your hands together and shiver if you’re describing something cold.<br/><br/>  

  Maintain a hand position that is open and welcoming.<br/><br/>  

  <b>Facial Expressions:</b>

  Similar rules apply.  If you’re describing something large, drag on the word large while expanding your eyes and slightly tilting your head back.<br/><br/>

  If you’re describing something serious you could lower your eyebrows, scrunch your face, and hit your hand with your fist.<br/><br/> 

  Happy:  Smile, eyebrows up<br/><br/>

  Sad: Frown, tilt your head down.<br/><br/> 

  Excited, angry, upset, surprised.  You understand my point?<br/><br/>  

  Match your words with your facial expression and smile to your audience.<br/><br/> 

  One thing to always remember is to smile.  Even though you may be nervous and you really just want to cry, mask your tears with a smile.  It may help control anxiety and nervousness as it tricks your mind into thinking you’re happy.  Not only could it trick your mind, it also tricks your audience into thinking you are happy, professional, and confident.<br/><br/>  

  <b>Eye contact:</b>

  Eye contact is essential in a presentation, speech, or in a general conversation.  Whether you’re speaking to someone one on one or you’re speaking to a small, medium, or large group, the more eye contact you have with different members of the audience, the better.<br/><br/> 

  Eye contact conveys sincerity, confidence, and trust.<br/><br/>  

  For instance, when you’re speaking to someone one on one, would you rather that individual look beyond you, to the left or right of you? Or would you prefer they look straight into your eyes?<br/><br/>   

  A tip when speaking to someone one on one is to go back and forth from each eye, or picture a T on his or her face and follow that T.  This will help with the uncomfortable feeling when looking someone straight in the eye.  Focus on moving to the left, right, and down the T.<br/><br/>   

  Avoid looking at the ground, ceiling, or beyond someone when having a conversation/when speaking to an audience.  Looking to the ground coveys a lack of confidence, looking beyond disconnects you from the audience, and looking at the ceiling is distracting.<br/><br/>   

  What message are you giving off if your head and eyes are gazed down, body is slouched, and your hands are in your pocket?  Nervous? Lack of confidence?  In addition to portraying this image of yourself, you may feel more nervous when presenting/giving your speech.<br/><br/>   

  Stand tall, look at your audience, and be proud you’re giving a speech/presentation in front of an audience.  Presenting is one of the most feared things that humans do, however, if the fear can be controlled, you will open your mind and your world to greater opportunities.<br/><br/> 

  The more comfortable you appear, the more confidence you will feel.<br/><br/> 

  A final note on eye contact.  When speaking to a small or medium sized group, shift your eyes from member to member and connect on a deeper level, especially the individuals who seem disengaged or distracted.  Avoid focusing on only one member or on one side of the room.<br/><br/> 

  Because we are presenting online, our focus is to look at the camera.  The camera is your audience, not the screen, the table, beyond the camera, or the walls.  Review your videos and watch for eye contact.<br/><br/>  

  <b>Stance</b><br/><br/>  

  When presenting or speaking to another individual, attempt to make your stance:<br/><br/>  

  •  Open
  •  Head up
  •  Shoulders back
  •  Avoid slouching
  •  Legs shoulder width apart.
  •  Lean slightly forward as it expresses passion and eagerness.<br/><br/>   

  As discussed, attempt to use your hands to enhance your message, however, if you do not need your hands, let them drop by your sides naturally.  Avoid placing your hands on you hips, back, having them crossed (unless part of your presentation), clenched together, in your pockets, or fiddling with an object, your clothes, or hair.<br/><br/>  

  When presenting with PowerPoint, avoid:<br/><br/>  

  •  Facing the screen and having your back to the audience.  Attempt to face your audience at all times,<br/><br/>   

  •  Reading the words straight off the slides.  PowerPoint should only used as a reference.  It’s a powerful tool to enhance your audiences’ comprehension and overall effect of your presentation.<br/><br/>   

  Experienced presenters will time their PowerPoint so well that they never have to look back at the screen.  You can accomplish this by being well prepared, having a strong understanding of the layout and flow of your presentation, and by using a clicker.<br/><br/>  

  We will go into PowerPoint and visual aids in much more detail in chapter 9.<br/><br/>  


  <b>Movement</b>

  Due to presenting online, you may choose to present from your chair or standing up.<br/><br/>  

  We would rather you present standing up to mimic the feeling of a real presentation, however, that is up to you.<br/><br/>  

  <b>Sitting:</b>

  Sit up, with your shoulders back, center yourself and focus on the camera, not the screen.  This will help you to become more comfortable when looking into one area
   (such as when looking into someone else’s eyes)<br/><br/>  

  <b>Avoid:</b>

  •  Moving from side-to-side in the chair
  •  Moving up and down in the chair
  •  Spinning in the chair<br/><br/>  

  <b>Standing:</b><br/><br/>  

  Ensure the camera is able to capture your face, head, width of your shoulders, and if you’re able to, the upper half of your body.  If you have the space, you should be able to move a few steps to the left and right, back and forth with the camera still focused on you.<br/><br/>    

  If you have the space online or if you’re presenting off-line to a live audience, attempt to move around addressing each section of the room.  By moving around the room, you allow yourself to connect with each member of the audience.<br/><br/>    

  If you’re making an important point, move your body close to the audience/camera emphasizing that point.<br/><br/>   

  Match your body gestures, facial expressions, and movement with your speech.<br/><br/>    

  Presenting and movement.  A suggestion:<br/><br/>    

  First:  Being your presentation in the middle with your opening.<br/><br/>    

  Second:  Move to the left or right for your first main point<br/><br/>  

  Third:  Move back to the center for your second main point<br/><br/>  

  Forth:  Move to the opposite direction of which you choose in step two for your third point<br/><br/>  

  Finally, move back to the center to close out your presentation/speech.<br/><br/>  

  Avoid discussing an important point when moving from one side of the room to another.  The movement is a physical transition.  You can use this time to pause, or begin into the first few words of your next sentence.<br/><br/>    

  When moving about the room, be careful not to scuff your feet, this can be distracting to your audience.<br/><br/>    

  <b>Note:</b>

  •  Avoid facing away from your audience
  •  Staying in one spot
  •  Only moving to one side of the room and not addressing the other 
  •  Hiding behind the lectern or podium unless necessary.<br/><br/>    

  One last point on “things to avoid”  - Avoid leaning up against the wall, someone else, the podium, or sitting down in a chair or on a table. This projects laziness and unprofessionalism.<br/><br/>  

  <b>Approach:</b> 

  1)	Break up your room into 3 parts and address each part equally with eye contact, movement, & body language.<br/><br/>    

  2)	Have a welcoming presence.  Open body, and hands.<br/><br/>  

  3)	Use hand gestures.  Avoid placing your hands in your pocket, clenched, or stuck to the side of your body in an unnatural manner.<br/><br/>  

  4)	Face the audience at all times<br/><br/>  

  Research indicates using hand gestures increases confidence in yourself and in your message.<br/><br/>  


  
