<p class="lead"><br/><br/>

  <b>Persuasive messages are opinions.</b><br/><br/>

  Whether we know it or not, we’re all salespeople in one way or another.  Whether you sell cars, a brand, electronics, an idea, a vacation spot to your spouse, an idea to your colleague, persuading your boss for a raise, requesting more time off, or if you’re influencing a group or nation to a potential course of action/solution, you’re a salesperson.  If not already, there will be times that you will have to persuade, convince, or influence someone to your position, viewpoint, or idea.<br/><br/>

  If you’re able to acquire, build, and hone the ability to persuade others, you will have developed a invaluable skills that will pay dividends time and time again.  People who can persuade, convince, and influence others are leaders, and will have more success in their future endeavors.  Having the ability to persuade others to your way of thinking while having these individuals act upon that thinking is a learned skill and a skill that should not be taken granted for.<br/><br/>

  Let’s dive in and learn how to speak more persuasively so we too can have this powerful skill.<br/><br/>

  The main goal of a persuasive message is for your audience to accept and act on your position, viewpoint, or idea.<br/><br/>

  Despite this not being an inspirational speech, you must generate energy and excitement in your words.  Use passion and body language to enter into the emotional drivers and trigger points of your audience.  Attempt to cater to their emotions and belief. Your strategy is to use your knowledge of the audiences’ opinions, beliefs, and frame of mind to your advantage while reinforcing their existing beliefs.  This will help you win over the approval and acceptance of your main message.<br/><br/>

  There are times when your main objective is to convince your audience of your position.  I.e – No one in the room is on your side, the room has no prior knowledge, or most of the people are against you.  Use energy, excitement, and prior knowledge of an unmet need to build your case in convincing the adoption of your viewpoint/ position.<br/><br/>

  Adding in stats, facts, testimonies, stories, examples, and illustrations are other ways to convince your audience.  Remember to include credible and reliable sources.<br/><br/>

  Ex – If you’re a car salesman, you could use stats, facts, testimonials, success stories from previous buyers to help reinforce your product/car.<br/><br/>


  <b>Guidelines for a Persuasive message</b><br/><br/>

  According to John Medina – ‘The brain does not favor boring information and speeches.  When the brain is stimulated with emotion, excitement, and energy, the amygdala releases dopamine into the body that helps in retention and information processing.  (Dopamine aids in memory and information processing)<br/><br/>

  Your goal is to plan and rehearse a presentation that contains an emotionally charged event.  Just remember the above information and if you’re unable to deliver such a presentation, try to at least build up the most important points you want your audience to leave with so your audience members will remember that charged up, important point long after your presentation.<br/><br/>

  Below are a few guidelines, and if followed, will help you in becoming more effective in persuading your audience.<br/><br/>

  1)	Qualifications – Are you experienced with this topic?  Do you know it inside and out?  How long have you been in the industry?  What types of supporting material do you have to back your main point/claim?  Are you able to simplify the information without simplifying?  Are you able to use multiple examples, analogies, and illustrations?<br/><br/>

  For instance, do you have past success?  Victories? A positive reputation?  Experience?   Make sure the audience is aware of your successes and experience.  I would recommend placing these in your opening to build initial credibility.  Example, Tiger Woods might not be successful in persuading an audience to adopt his viewpoint on relationships; however, he would be successful in teaching someone how to golf.<br/><br/>

  Therefore, If I were Tiger woods, I would begin stating my name, why I’m here, and why I’m qualified to teach you how to golf.  I.e – Championships, tournament wins, # 1 in the world, experience, the list goes on.<br/><br/> 

  Have conviction and sincerity in serving your audience members first.  Are you speaking from your audiences’ frame of mind, their self-interest?  Do you really want to help the audience or are you merely doing this for personal gain?  If the audience detects this they may push back and resist.<br/><br/>

  2)	Confidence – With knowledge, past successes, victories, sincerity, and a positive reputation you should have the delivery of success.  Avoid or mask the appearance of being timid and nervous as this can harm your delivery and outcome.  Speaking in front of an audience can be a traumatic experience for many; however, being an expert in a particular field usually decreases the anxiety and nervousness, as you know more than your audience.  The audience that is being persuaded, convinced or sold are looking for someone who will lead them to a better place, someone who is strong and firm.  Use your knowledge from past lessons on eye contact, body language to help with your delivery.<br/><br/>

  If you’re presenting in a small to medium sized room, attempt to connect your eyes with different audience members throughout your presentation.  Research indicates eye shifting/movement could be signs of deception, lack of confidence, and insecurities.  This is important for small groups and one on one conversation.  As discussed earlier, look others in the eye.  Not too long as it could make that individual in the audience uncomfortable, however, long enough that you ensure there was a connection between you and that individual.<br/><br/>

  A big variable in your persuasive message will be:<br/><br/>

  •  Who makes up your audience and what are their opinions, interests, beliefs, level of knowledge, and attitudes.<br/><br/>

  Majority of the time you will have an audience that is mixed.<br/><br/>

  Therefore, it’s your job to tailor your message in a way that reinforces the individuals in the audience who agree with you and persuade and convince the members who are on the fence.  You could use stats, facts, passion, testimonials, and stories that adhere to the ego’s, interests of the hostile, non-believers in a way that doesn’t offend or puts them on the attack.<br/><br/>

  Hostile members will be ready to attack, which could ruin your entire presentation.  Most of the time you will be able to identify them in your audience.  If it’s a large audience in which each member is unable to speak to the whole, then their hostile behavior is less powerful.  
  If it’s a room where all members can communicate freely, you must identify and stroke these individuals ego’s and concerns while addressing their issues in a friendly and sincere manner.  Your goal is to cover them with kindness so they will lay off.<br/><br/>

  If you have a believer in the crowd, try engaging with this individual as much as you can.  The more you have this audience member engaged and adding to your position, the better.<br/><br/>

  The last group of individuals that could make up your audience are:<br/><br/>

  •  Individuals who could care less 
  •  Individuals who are uninformed.<br/><br/>

  With the uninformed, you must provide enough information so they can weigh the benefits over choosing your side compared to not choosing your side.  The information that you delivery has to provide enough merit and positive outcomes for these individuals to choose your position, purchase your product/service, or viewpoint.<br/><br/>  

  Now for individuals who could care less  - Your job is to use your passion & information, stats, facts, stories, proof, that will convince these individuals of the importance of your position.  Maybe they never knew about something that affects them, or the benefits your position, product/service, viewpoint could have on their life.<br/><br/>

  As discussed, you should attempt to use stories, proof, evidence, stats, and facts to begin the change.  You should attempt to adhere to the audiences past beliefs while providing enough information to help them adopt your position.  Always speak in terms of how your position could help the audience; never speak in terms of how it could benefit you.<br/><br/>  

  Use emotional trigger points to help persuade your audience.  Emotions such as: anger, sadness, happiness, and love.  These strong emotions can help you in your path for changing beliefs.<br/><br/>

  Think how you feel when you’re really sad, how much would you pay to be relieved of this sadness?  What if you had a solution to this particular pain point?  Bring your audience back to the scene of the crime, back to the sadness; make them feel what that pain feels like.  With such high emotion, you then introduce your solution/position that could help avoid/fix this issues.<br/><br/>


  <b>Organizing your Persuasive message</b>

  1)	State the problem, present the solution.  This style of organization works best when presenting to groups, a board, or council.<br/><br/>

  Opening:  Choose to state the problem, or state the problem and solution<br/><br/>

  Body:  Either the solution or solutions.  The way this solution or solutions will be the answer to your audiences’ problems.  Alternatively, if you choose to state the problem and solution in the opening, you could then devote the body to the benefits of the solution, why it’s the best solution, how to go about putting this solution into effect.  Provide 2-3 main points/claims with supporting material.  Each main point should be backed up with at least two points of supporting material.<br/><br/>

  Ex – You could open by stating the problem:  “The recent increase in taxes”.  A fact about the most recent surge in tax rates and your solution to this issue.<br/><br/>

  1st point:  How to put it into effect
  Sub point
  Detail - Supporting material
  i.  
  ii.<br/><br/>

  2nd point:  Obstacles & how we will overcome these obstacles
  Sub point
  Detail - Supporting material
  i.
  ii.<br/><br/>

  3rd point:  How we will benefit, the brighter future.
  Sub point
  Detail - Supporting material
  i.
  ii.<br/><br/>

  Close:  Sum up your main points and ask for support?<br/><br/>

  As you can see I stated the problem and solution in the opening.  My goal is to rattle my audiences’ emotions, and bring the issue to the forefront.  The body was used to convince the audience through a plan of action, obstacles that we will overcome, and what the future will look like with reduced taxes.<br/><br/>  

  There are many ways you can construct the problem/solution, however, ensure you capture and stimulate emotions off the start.  Provide stats, stories, facts, and why you’re the individual that can provide the solution.  Prove and provide your credibility to the audience.  Use the body to either explain your solution, how it will benefit the audience, a vision of a better future/life while tailoring your message to the audiences self interest.<br/><br/>

  Listed below are a few examples:<br/><br/>

  #1 - Statement of proposition - Proposition and proof that supports your statement.
  I’ve stated three main points/claims.  You can choose to use two.<br/><br/>

  Example – Our employees are looking to take Friday afternoon off to increase team moral, productivity, and overall job satisfaction.<br/><br/>  

  1st point:  Decrease in productivity:  
  Sub point -<br/><br/> 

  i. Research indicates that employees’ productivity decreases by 75% once Friday afternoon arrives.  Briefly explain research.
  ii. Why does this occur?<br/><br/>

  2nd point:  Increase in job satisfaction:  
  Sub point -<br/><br/> 

  i.  Research indicates having Friday afternoon off increases employees overall happiness, which will increase productivity throughout the week.  Explain the claim.  Where did this research come from?
  ii.  Why does it increase happiness?  Why is this healthy?  Short term and long term?<br/><br/>

  3rd point:  Decrease in recruiting/training fees:
  Sub point -<br/><br/> 

  i.  Stats from “Source” indicates that it costs ” X - $ “ to recruit and train a new employee.  Explain source.<br/><br/> 

  ii.  How does this costs affect the company?<br/><br/> 

  Close:  Sum up, review you main points and ask for approval or trial of your proposal.<br/><br/> 

  #2 - Compare and contrast:  State the problem, compare, and contrast your solution to the current solution or non-existent solution.  Alternatively, you could state the problem and compare the advantages and disadvantages of your solution/proposal.<br/><br/>  

  # 3 - Dr. Alan H. Monroe – Motivational sequence.  Below is a sequence in persuading an individual or audience to your position, developed by Dr. Alan Monre.  
  http://en.wikipedia.org/wiki/Monroe's_motivated_sequence<br/><br/>


               (Opening)<br/><br/>

  1)	Attention:  Capture the audience attention with an opening statement, story, or quotation.<br/><br/>

               (Opening)<br/><br/>

  2)	Need:  Why there needs to be change and why it is important for the listeners and their self interest.  You could state a fact, stat, or example. 
  (Body)<br/><br/>

  3)	Satisfaction:  Present your solution with evidence and supporting material. (Body)<br/><br/>

  4)	Visualization:  Draw a picture in the minds of your audience of a better future.  What will things be like when the audience adopts your solution?  How will their lives benefit?  Describe in detail what this life will look like.<br/><br/>

  (Close)<br/><br/>

  5)	Action:  By the time you get to the call to action you will have an idea of the response.  Were the audience members agreeing with you?  Smiling?  Nodding?  Saying “yes” – whether you’re reading these gestures or if you’re receiving the opposite, you must ask for signatures, the sale, commitment.<br/><br/>

  You’ve inspired and convinced your audience to your viewpoint/position, you must propose a call to action.  You’ve spent all that time and energy adopting your audiences position, now it’s time to ask them for a signature, a trial, the sale, sign up for your newsletter.  Avoid walking away without receiving something in return, or at least asking.  Turn your hard work into a positive outcome for you and your audience.<br/><br/> 


  This sequence can work on many different formats for presentations and speeches.  Whether you’re a salesperson, an individual proposing a new amendment, convincing someone (child, spouse, boss, colleague, friend), it’s a proven technique and when implemented properly, a powerful tool in persuading an audience to your way of thinking.<br/><br/>

  
  
