<p class="lead"><br/><br/>

  The most common inspirational speeches are:<br/><br/>

  •  Self-help speeches
  •  Inspirational speeches – Tony Robbins, Zig Ziggler
  •  Business motivational speeches
  •  Team inspirational speeches – “Friday Night Lights”
  •  Political rally speeches<br/><br/>

  Attempt to connect with your listeners on a higher level, bring their fears, goals, and motives to the forefront while appealing and being empathetic to their self-interest, motives, and emotions<br/><br/>

  Once you have made a connection, you could then provide reasons why their current situation is inadequate and why we want to achieve a higher level of success and improvement.<br/><br/>

  Empathize with your audience; provide a story of how you were once in their shoes (this helps build your credibility).  Proceed into a follow up plan, a plan of action, how can we adopt this higher level of achievement, change, and success.  You must challenge your audience to take action, provide the benefits, what will their life be like when feeling and achieving this success?<br/><br/>

  Attempt to connect on a deep emotional level.  Your descriptions of this higher level of success, the visualization of success should be so prominent that once they leave the room they have the needed motivation to achieve this success and change.<br/><br/> 

  As discussed, your main goal in an inspirational speech is to connect with the audience.  To inspire change from a desire, a need, issues your audiences members are currently dealing with, faced in the past, or any issues they are/were unaware of.<br/><br/>  

  Psychologist Abraham Maslow identified five human needs.  Your goal is to connect and use these needs in your speech.<br/><br/>  


  https://en.wikipedia.org/wiki/File:Maslow%27s_Hierarchy_of_Needs.svg<br/><br/>  


  You want to have a good understanding of your audience, their values, beliefs, and interests to allow for a proper inspirational speech.<br/><br/>  

  Think about these questions:<br/><br/>  

  •  Who are my audience members
  •  What are their problems/issues
  •  What are their backgrounds?
  •  What are their thoughts?  What will they think when I say this?
  What will help them?  How will they benefit?<br/><br/>  

  Not only is this checklist an excellent tool when addressing an audience but an excellent tool when addressing just about anyone.<br/><br/>   

  •  Who am I speaking to?  A friend, a colleague, my boss?
  •  What makes them tick?  How can I get them talking about themselves?  What interests them?  
  •  What background did they come from?  How are their beliefs different from mine? others in the room?
  •  What are they thinking right now?  What will they think of me when I say this?<br/><br/>   

  Thinking with these questions in mind before a meeting, an interview, a friendly interaction, or with a co-worker will help you gain a better perspective of the other individual.  These types of questions help increase the strength of relationships, persuasion, and like-ability.<br/><br/>   

  Keep in the mind the 5 physiological needs discussed earlier:<br/><br/>  

  For example:<br/><br/>  

  What if you’ve been ask to present/speak among a group of aspiring entrepreneurs and your goal is to inspire them to go after their goals of starting a small business and escaping the 9-5.<br/><br/>   

  What approach could you take?  Well, let’s start to think in terms of what the audience is thinking<br/><br/>  

  For instance,<br/><br/>  

  •  The audience could be thinking:  “Will I have enough money to provide to my family?   -- Safety needs<br/><br/>  

  •  If I proceed and spend all my time working on my dream job, will I loose key connections with friends, my children, family members?  - Belongingness/love<br/><br/>  

  •  What if I fail?  What will people think of me?  My colleagues, family members, friends?  - Esteem<br/><br/>  

  •  What are my areas of growth?  What would life be like if I had freedom of time, more time to spend with my family and friends, escaping the boss and structure of working for someone else?  -- Experienced gained, self-actualization.<br/><br/>  

  To adhere to these concerns you must attempt to address them individually to ensure the audience will listen and be inspired for change.  You can address each separately while explaining how and why changes will occur, or you could discuss the changes that will occur, why they happen, and how their lives will change for the better.<br/><br/>  

  You could discuss how our current way of living is inadequate and explain how and why change will occur for the better.  Explain going against the status quo and the feelings that will be felt once doing this.<br/><br/>  

  You could discuss starting slow, keeping your current job, and begin to work on your small business in the morning, evening/weekends.  
  Build enough interest and capital in your business first while maintaining your job to offset the risk.<br/><br/>  

  Create a plan and strategy for the week:
  How many hours will I spend on my project?
  How many hours will I spend with family and friends?
  How many hours will I be at work?<br/><br/>  

  You could discuss that most people will respect you, compared to criticizing you for actually going for your dreams.  Discuss starting slow, building enough interest, keeping what you’re doing semi-privately at the beginning to avoid the added pressure.<br/><br/>   

  Paint a picture of what life would be like without working for someone else, the feeling of freedom.  Sleeping in Monday mornings.<br/><br/>  

  Once you’ve tapped into your audiences concerns and interest you will have more success in inspiring change.<br/><br/>  

  Example:<br/><br/>  

  Open:  Addressing the need for change.  A story of the issues, a dramatic act of what life was like, and what it could be.<br/><br/>  

  Address the audience<br/><br/>  

  Then move on to your first message/main point.<br/><br/>  

  1st main point:  Address the psychological needs of your audience.  “Why should we fear working for ourselves?  There are many ways to deal with the risk and constraints of starting a small business…..”<br/><br/>  

  2nd main point:  You then could explain how and why these changes will occur and explain why the change will be for the better.  You could then further explain how their current situation may not be the best possible situation; it could be inadequate or counterproductive.<br/><br/>  

  3rd main point:  For your third main point you could inspire the audience with a desire for change.  What they can do to begin this change, what will life look like? The advantages?  Attempt to paint a picture of success, describe the feeling of victory through connecting with their emotions, beliefs, and values.   You could also discuss the potential struggles that they might expected, and how to overcome when faced.<br/><br/>  

  Close:  Sum up the benefits with energy and excitement.  Challenge listeners to adopt this change.<br/><br/>  

  Example close:<br/><br/>   

  “I want you all to leave here today and make the first step in fulfilling your dreams and escaping the 9-5.  Register a business name, buy a domain name, make a business plan.  Start small, but initiate the change!  Think of the possibilities…”<br/><br/>  

  Remember that an inspirational speech is all about how you address the audience with your words, body language, tone, and movement.<br/><br/>  

  Use strong stories, anecdotes, and testimonials to connect on a human level.  People want to hear success stories, how others overcame struggles, what life is like when achieving success.<br/><br/>  

  Create a bond between you and your listener.  Always keep their self-interest in mind.  Your speech has nothing to do with you benefitting.<br/><br/>  

  Use descriptive words that help paint a picture in the minds of your audience.  What will success feel like?  What will sleeping in Monday mornings feel like?<br/><br/>  

  Use powerful, positive words to cause action.  When describing potential struggles, speak about these struggles in the most positive way possible.  Follow a struggle with a positive story or outcome.<br/><br/>    


  <b>Active listening</b><br/><br/>  

Wikipedia states that Active listening is a communication technique that requires the listener to feed back what they hear to the speaker, by way of re-stating or paraphrasing what they have heard in their own words, to confirm what they have heard and moreover, to confirm the understanding of both parties.<br/><br/>  

  That’s exactly it, when you’re actively listening to someone, you’re actually listening to that other individual and not:<br/><br/>  

  •  Preparing your next sentence in your mind
  •  Preparing to blurt out your opinion, input, or suggestions at the moment your counterpart pauses
  •  Looking away, beyond, at the floor, or another individual.
  •  Speaking while the other person is speaking.<br/><br/>  

  Active listening could seem less trivial than speaking and presenting, but it could be one of the most important skills for building relationships, respect, and leadership.<br/><br/>   

  Your goal with active listening is to:  Listen; focus on the individual and when the conversation is over, you could restate in your own words what the other individual said.<br/><br/>   

  So many of us want to speak and share our input, but are we really listening?<br/><br/>  

  Whether you’re communicating to a colleague, boss, professor, classmate, family member or friend, attempt to listen to the words that are being conveyed.<br/><br/>  

  In your next conversation, attempt to create an atmosphere of open communication and active listening by:<br/><br/>  

  Focus - Focus on the other individual.  Stop whatever you’re doing and listen.  Avoid looking away, beyond the individual, at the ceiling, or floor.<br/><br/>  

  Eye contact - Maintain eye contact.  Research indicates that you build trust and respect when connecting and maintaining eye contact with the person you’re talking to.<br/><br/>  

  Questions - Ask questions and listen to the response.  Remember to wait until the other individual has completed their sentence or thought.  If you do not understand something:  Wait until the other person has finished that thought and ask him/her to repeat that last sentence or the area you found confusing.  This shows that you care about what they’re saying and you want a full grasp onto their message.  Questions are an excellent way to show that you listen.  Questions allow you to uncover needs, additional information, or further insight into the conversation.  Questions also allow the other individual to elaborate on their message, it shows that you care and appreciate their input.<br/><br/>  

  Listen - Instead of thinking about what you’ll say next, or thinking about the night before, or what you’re going to eat for dinner, be conscious of this and attempt to regain focus onto what the other person is saying.<br/><br/>    

  Body Language - Face the individual.  Use open body language.  Lean in, sit up, and focus on the speaker.  Avoid crossing your arms, slouching, or fiddling with your hair, pen, or clothes.<br/><br/>    

  Key words - Listen for key words within the speakers message so you’re able to carry on the conversation after they’re finished speaking.<br/><br/>  

  Open mind - Avoid entering into any conversation with judgments, previous opinions, or prejudices.  Listen to the other individual and try to understand their side of the conversation and message.  If you’re having trouble with understanding their view point, ask them questions for further comprehension.  Avoid disagreeing with the individual while they’re speaking.  Let them finish, clarify, or at the right opportunity:  ask a question for further insight into the area of the message that you do not agree with.<br/><br/>  

   Non-verbal cues - During the conversation/interaction, nod your head subtly to show you understand, raise your eyebrows at interesting points.<br/><br/>  

  Summarize - Depending on the conversation, i.e – If it’s an interview, a sales call, an important meeting or a general conversation with a client, boss, or colleague: try to confirm by restating or summarizing the main points within the conversation.<br/><br/>   

  Note:  If conducive to the interaction, ask if it’s okay to take notes while having this conversation/meeting.  Most people will appreciate the fact that you want to take thoroughly notes of the points discussed.<br/><br/>  

  Thank you - If it’s an important meeting/conversation, ensure to thank that individual for taking the time out of their day to provide you with that information/ have that conversation with you.<br/><br/>    

  Remember:  To build strong relationships:<br/><br/>  

  •   Sincerely listen to the other individual
  •   Let them speak more than you
  •   Be genuinely interested into what the other individual has to say.
  •   Restate and summarize the main points
  •   Ask questions<br/><br/>  

  Remember:  In business and in life, you will always learn more when you listen than when you speak.<br/><br/>   

  
